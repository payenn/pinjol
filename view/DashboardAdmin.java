    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        NasabahController nc = new NasabahController();
        String a= this.txtNama.getText();
        String b=this.txtHp.getText();
        double c= Double.parseDouble(this.txtJumlah.getText());
        
        boolean status = nc.tambahNasabah(a, b, c);
        if(status==true){
            JOptionPane.showMessageDialog(this, "Data Berhasil Ditambah");
            //agar data yang baru diinput update di jtable
            DefaultTableModel dtm2 = nc.createTable();
            
            //menerapkan desain dtm di controller  pada table
            this.tblNasabah.setModel(dtm2);
            this.tbUNasabah.setModel(dtm2);
            this.tbHNasabah.setModel(dtm2);
            //mengisi tabel nasabah dengan data hasil query
            nc.TampilkanNasabah();
        }else{
            JOptionPane.showMessageDialog(this, "Data Gagal Disimpan");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        NasabahController nc = new NasabahController();
        int a = Integer.parseInt(lblUId.getText());
        String b = this.txtUNama.getText();
        String c = this.txtUHp.getText();
        double d = Double.parseDouble(this.txtUJumlah.getText());
        
        boolean status = nc.ubahNasabah(a, b, c, d);
        if(status==true){
            //agar data yang baru diinput update di jtable
            DefaultTableModel dtm2 = nc.createTable();
            
            //menerapkan desain dtm di controller  pada table
            this.tblNasabah.setModel(dtm2);
            this.tbUNasabah.setModel(dtm2);
            this.tbHNasabah.setModel(dtm2);
            //mengisi tabel nasabah dengan data hasil query
            nc.TampilkanNasabah();
            JOptionPane.showMessageDialog(this, "Berhasil Diubah");
        }else{
            JOptionPane.showMessageDialog(this, "Gagal Ubah");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void tbUNasabahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUNasabahMouseClicked
        // TODO add your handling code here:
        //Konversi tbUnasabah dalam bentuk dtm
        DefaultTableModel dtm2 = (DefaultTableModel) tbUNasabah.getModel();
        
        //mengambil nilai 1 barus / record dari yang di click
        int pilih = tbUNasabah.getSelectedRow();
        
        //nilai yang dipilih dimasukan ke text field
        //pilih = baris, 0-3=kolom
        this.lblUId.setText(dtm2.getValueAt(pilih, 0).toString());
        this.txtUNama.setText(dtm2.getValueAt(pilih, 1).toString());
        this.txtUHp.setText(dtm2.getValueAt(pilih, 2).toString());
        this.txtUJumlah.setText(dtm2.getValueAt(pilih, 3).toString());
        
    }//GEN-LAST:event_tbUNasabahMouseClicked

