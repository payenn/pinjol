    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        NasabahController nc = new NasabahController();
        int a = Integer.parseInt(this.lblHId.getText());
        boolean status = nc.hapusNasabah(a);
        
        if(status==true){
            //agar data yang baru diinput update di jtable
            DefaultTableModel dtm2 = nc.createTable();
            
            //menerapkan desain dtm di controller  pada table
            this.tblNasabah.setModel(dtm2);
            this.tbUNasabah.setModel(dtm2);
            this.tbHNasabah.setModel(dtm2);
            //mengisi tabel nasabah dengan data hasil query
            nc.TampilkanNasabah();
            JOptionPane.showMessageDialog(this,"Berhasil Dihapus");
        }else{
            JOptionPane.showMessageDialog(this, "Gagal Dihapus");
        }
    }//GEN-LAST:event_jButton10ActionPerformed

